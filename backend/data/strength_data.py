def get_strengths_by_virtue(virtue_code: str) -> List[Dict[str, Any]]:
    """
    获取指定美德类别下的所有优势
    
    Args:
        virtue_code: 美德类别代码
    
    Returns:
        该类别下的所有优势定义
    """
    virtue = VIRTUE_CATEGORIES.get(virtue_code)
    if not virtue:
        return []
    return [VIA_STRENGTHS[key] for key in virtue["strengths"] if key in VIA_STRENGTHS]


def get_all_strength_keys() -> List[str]:
    """
    获取所有优势的key列表
    
    Returns:
        所有优势代码列表
    """
    return list(VIA_STRENGTHS.keys())


